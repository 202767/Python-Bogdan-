my_nums = [10, 'abs', 10.3, True, None]  # создание списка1

my_nums.pop(2)  # удаление 3 элемента

print(len(my_nums))  # вывод длины нового списка

my_nums.reverse()  # изменение порядка следования эл списка

other_nums = ['def', False]  # создание списка2

all_nums = my_nums + other_nums  # расширение списка1 элементами списка2

print(all_nums)  # вывод в терминал конечного списка
